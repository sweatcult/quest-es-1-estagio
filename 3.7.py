def soma_numeros():
    # solicita dois números inteiros 
    num1 = int(input("Digite o primeiro número: "))
    num2 = int(input("Digite o segundo número: "))
    # calcula a soma de dois números
    soma = num1 + num2
    # imprime a soma
    print("A soma dos dois números é:", soma)
# chama a função
soma_numeros()
