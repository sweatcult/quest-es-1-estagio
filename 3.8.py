def me_para_mi():
    # solicita um valor em metros do usuário
    metros = float(input("Digite o valor em metros: "))
    # converte o valor para milímetros
    milimetros = metros * 1000
    # imprime o valor convertido
    print(f"O valor em milímetros é: {milimetros}mm")
# chama a função
me_para_mi()
